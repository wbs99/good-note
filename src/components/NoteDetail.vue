<template>
  <div id="note-detail">
    <h1>{{ msg }}:{{ $route.params.noteId }}</h1>
  </div>
</template>

<script>
import Auth from '../apis/auth'
export default {
  data() {
    return {
      msg: '笔记本详情页'
    }
  },
  created() {
    //获取用户信息，未登录跳转到登录页
    Auth.getInfo()
      .then(res => {
        if (!res.isLogin) {
          this.$router.push({ path: '/login' })
        }
      })
  }
}
</script>

<style scoped>
h1 {
  color: red;
}
</style>